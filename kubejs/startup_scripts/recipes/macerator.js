function addMaceratorRecipes(material, tier) {
    if(!tier) tier = 1;

    // dust from ingot
    addMaceratorRecipe(`kubejs:${material}_dust`, `kubejs:${material}_ingot`, 200, tier)
        
    // dust from plate
    addMaceratorRecipe(`kubejs:${material}_dust`, `kubejs:${material}_plate`, 200, tier)
        
    // dust from rod
    addMaceratorRecipe(`kubejs:${material}_dust`, `3x kubejs:${material}_rod`, 200, tier)
        
    // dust from bolt
    addMaceratorRecipe(`kubejs:${material}_dust`, `6x kubejs:${material}_bolt`, 200, tier)

    // dust from curved plate
    addMaceratorRecipe(`2x kubejs:${material}_dust`, `kubejs:${material}_curved_plate`, 200, tier)

    // dust from ring
    addMaceratorRecipe(`kubejs:${material}_dust`, `3x kubejs:${material}_ring`, 200, tier)

    // dust from gear
    addMaceratorRecipe(`4x kubejs:${material}_dust`, `kubejs:${material}_gear`, 200, tier)

    // dust from rotor
    addMaceratorRecipe(`8x kubejs:${material}_dust`, `kubejs:${material}_rotor`, 200, tier)
        
    // dust from wire
    addMaceratorRecipe(`kubejs:${material}_dust`, `6x kubejs:${material}_wire`, 200, tier)
}

